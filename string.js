let text = "JavaScript is Awesome";

console.log("Length of the string:",text.length);

console.log("Character at index 5:",text.charAt(5));

console.log("Unicode value of character at index 5:",text.charCodeAt(5));

console.log("Last character of the string:",text.charAt(text.length - 1));

console.log("Character at index 3:",text.charAt(3));

console.log("Uppercase:",text.toUpperCase());

console.log("Lowercase:",text.toLowerCase());

console.log("Concatenated string:",text.concat(" Let's Learn"));

console.log("Trimmed string:",text.trim());

console.log("Trimmed from start:",text.trimStart());

console.log("repeated string",text.repeat(2));

console.log(" Replace 'Awesome' with 'Cool':", text.replace("Awesome", "Cool"));






